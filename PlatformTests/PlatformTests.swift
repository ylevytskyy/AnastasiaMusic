import XCTest
import RxSwift

@testable import Platform

class PlatformTests: XCTestCase {
    func testExample() {
        let useCaseProvider = UseCaseProvider()
        let useCase = useCaseProvider.makeSongUseCase()
        let disposeBag = DisposeBag()
        
        let expectation = XCTestExpectation(description: "Search For Song")
        useCase.search(query: "Depeche Mode")
            .debug("songs", trimOutput: false)
            .subscribe(onCompleted: { expectation.fulfill() })
            .disposed(by: disposeBag)
        wait(for: [expectation], timeout: 7.0)
    }
}
